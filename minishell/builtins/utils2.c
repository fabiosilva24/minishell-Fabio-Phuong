int	ft_len_eq(char *str)
{
	int	i;

	i = 0;
	while (str[i] != '=' && str[i] != '\0')
		i++;
	return (i);
}

int	max(int a, int b)
{
	if (a > b)
		return (a);
	else
		return (b);
}
